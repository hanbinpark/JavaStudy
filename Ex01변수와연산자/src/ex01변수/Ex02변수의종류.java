package ex01변수;

public class Ex02변수의종류 {
	// main 단축키: main
	public static void main(String[] args) {
		// Java 변수의 종류
		
		// 정수형 데이터 타입
		// byte - 1byte(-128~127) = 8bit
		byte num1 = 127;
		
		// short - 2byte
		short num2 = 5000;
		
		// int - 4byte
		int num3 = 2044444444;
		
		// long - 8byte
		long num4 = 3000000000l;
		
		// 실수형 데이터 타입
		// float - 4byte
		// 숫자 뒤에 f or F 입력
		float num5 = 14.3f;
		// 같은 표현
		// float num5 = (float)14.3;
		
		// double - 8byte
		double num6 = 3.14;
		
		// 논리형 데이터 타입
		// boolean - 1bit or 1byte
		boolean isCheck = true;
		
		// 문자형 데이터 타입
		// char - 2byte
		// 무조건 ''
		char text = 'A';
		
		System.out.println(text + 1);
		System.out.println((char)44033);
		
		// 변수의 규칙
		
	}


}
